# Belajar Elif


menuPilihan = input("Silahkan pilih menu [1-3]: ")

if menuPilihan == "1":
    print("Menu anda adalah 1, yaitu ayam goreng")
elif menuPilihan == "2":
    print("Menu anda adalah 2, yaitu ayam bakar")
elif menuPilihan == "3":
    print("Menu anda adalah 3, yaitu ayam bacem")
else:
    print("Menu Tidak Tersedia")

if menuPilihan == "x":
    print("Program Keluar")

